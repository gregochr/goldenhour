-- Durable evaluation cache — persists briefing drill-down scores across restarts.
-- One row per cache key (regionName|date|targetType), upserted on each write.

CREATE TABLE cached_evaluation (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cache_key        VARCHAR(255) NOT NULL UNIQUE,
    region_name      VARCHAR(100) NOT NULL,
    evaluation_date  DATE NOT NULL,
    target_type      VARCHAR(50) NOT NULL,
    results_json     TEXT NOT NULL,
    source           VARCHAR(20) NOT NULL,
    evaluated_at     TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at       TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_cached_evaluation_date
    ON cached_evaluation(evaluation_date);
CREATE INDEX idx_cached_evaluation_region_date
    ON cached_evaluation(region_name, evaluation_date);
