-- V44: Prompt regression testing tables

-- Prompt test run: one row per triggered prompt test
CREATE TABLE prompt_test_run (
    id                          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    started_at                  TIMESTAMP    NOT NULL,
    completed_at                TIMESTAMP,
    duration_ms                 BIGINT,
    target_date                 DATE         NOT NULL,
    target_type                 VARCHAR(10)  NOT NULL,
    evaluation_model            VARCHAR(10)  NOT NULL,
    locations_count             INT          NOT NULL DEFAULT 0,
    succeeded                   INT          NOT NULL DEFAULT 0,
    failed                      INT          NOT NULL DEFAULT 0,
    total_cost_pence            INT          NOT NULL DEFAULT 0,
    total_cost_micro_dollars    BIGINT,
    exchange_rate_gbp_per_usd   DOUBLE,
    parent_run_id               BIGINT,
    git_commit_hash             VARCHAR(40),
    git_commit_date             TIMESTAMP,
    git_dirty                   BOOLEAN,
    git_branch                  VARCHAR(255),
    CONSTRAINT fk_ptr_parent FOREIGN KEY (parent_run_id) REFERENCES prompt_test_run(id)
);

-- Prompt test result: one row per location evaluation
CREATE TABLE prompt_test_result (
    id                              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    test_run_id                     BIGINT       NOT NULL,
    location_id                     BIGINT       NOT NULL,
    location_name                   VARCHAR(255) NOT NULL,
    target_date                     DATE         NOT NULL,
    target_type                     VARCHAR(10)  NOT NULL,
    evaluation_model                VARCHAR(10)  NOT NULL,
    rating                          INT,
    fiery_sky_potential             INT,
    golden_hour_potential           INT,
    summary                         VARCHAR(1000),
    prompt_sent                     CLOB,
    response_json                   CLOB,
    atmospheric_data_json           CLOB,
    duration_ms                     BIGINT,
    cost_pence                      INT          NOT NULL DEFAULT 0,
    cost_micro_dollars              BIGINT,
    input_tokens                    BIGINT,
    output_tokens                   BIGINT,
    cache_creation_input_tokens     BIGINT,
    cache_read_input_tokens         BIGINT,
    succeeded                       BOOLEAN      NOT NULL DEFAULT FALSE,
    error_message                   VARCHAR(500),
    created_at                      TIMESTAMP    NOT NULL,
    low_cloud_percent               INT,
    mid_cloud_percent               INT,
    high_cloud_percent              INT,
    visibility_metres               INT,
    wind_speed_ms                   DECIMAL(5,2),
    wind_direction_degrees          INT,
    precipitation_mm                DECIMAL(5,2),
    humidity_percent                INT,
    weather_code                    INT,
    pm25                            DECIMAL(6,2),
    dust_ugm3                       DECIMAL(6,2),
    aerosol_optical_depth           DECIMAL(5,3),
    temperature_celsius             DOUBLE,
    apparent_temperature_celsius    DOUBLE,
    precipitation_probability       INT,
    tide_state                      VARCHAR(20),
    tide_aligned                    BOOLEAN,
    CONSTRAINT fk_ptres_test_run FOREIGN KEY (test_run_id) REFERENCES prompt_test_run(id) ON DELETE CASCADE,
    CONSTRAINT fk_ptres_location FOREIGN KEY (location_id) REFERENCES locations(id)
);
