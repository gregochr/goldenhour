-- V117: Sky-rating eval persistence — the historian for the pass^k calibration eval.
--
-- The gated SkyRatingEvalTest is the CI pass/fail gate; this is its longitudinal
-- counterpart. An app-side service runs the same frozen fixtures through the real
-- scorer on a cadence and persists every run here so calibration DRIFT can be
-- graphed over time (rating vs band per fixture, sub-score drift, pass-rate trend),
-- and a step-change attributed to the prompt edit (git_commit_hash) that caused it.
--
-- Two tables mirror the prompt_test_run / prompt_test_result grain: one parent row
-- per eval invocation, one child row per (fixture x run_index). Sub-scores
-- (fiery_sky, golden_hour) are stored alongside the 1-5 rating because the rating
-- is coarse — calibration can drift 10 points under a star before it flips, so the
-- 0-100 scores are the early-warning line. The band (expected_min/max) is stored on
-- each result so a graph can plot rating against its own ground-truth band.

-- Parent: one row per triggered eval run.
CREATE TABLE sky_rating_eval_run (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_timestamp           TIMESTAMP        NOT NULL,
    started_at              TIMESTAMP        NOT NULL,
    completed_at            TIMESTAMP,
    duration_ms             BIGINT,
    model                   VARCHAR(10)      NOT NULL,
    runs_per_fixture        INT              NOT NULL,
    fixture_count           INT              NOT NULL DEFAULT 0,
    total_runs              INT              NOT NULL DEFAULT 0,
    total_passes            INT              NOT NULL DEFAULT 0,
    below_misses            INT              NOT NULL DEFAULT 0,
    above_misses            INT              NOT NULL DEFAULT 0,
    pass_rate               DOUBLE PRECISION NOT NULL DEFAULT 0,
    input_tokens            BIGINT           NOT NULL DEFAULT 0,
    output_tokens           BIGINT           NOT NULL DEFAULT 0,
    cost_micro_dollars      BIGINT           NOT NULL DEFAULT 0,
    trigger_source          VARCHAR(20)      NOT NULL,
    status                  VARCHAR(20)      NOT NULL,
    error_message           TEXT,
    git_commit_hash         VARCHAR(40),
    git_commit_date         TIMESTAMP,
    git_dirty               BOOLEAN,
    git_branch              VARCHAR(255)
);

-- Child: one row per (fixture x run_index) within a run.
CREATE TABLE sky_rating_eval_result (
    id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id                  BIGINT       NOT NULL,
    fixture_name            VARCHAR(80)  NOT NULL,
    run_index               INT          NOT NULL,
    rating                  INT,
    fiery_sky               INT,
    golden_hour             INT,
    expected_min            INT          NOT NULL,
    expected_max            INT          NOT NULL,
    miss_direction          VARCHAR(10),
    summary                 TEXT,
    input_tokens            BIGINT,
    output_tokens           BIGINT,
    duration_ms             BIGINT,
    CONSTRAINT fk_srer_run FOREIGN KEY (run_id) REFERENCES sky_rating_eval_run(id)
);

CREATE INDEX idx_srer_run ON sky_rating_eval_result(run_id);
CREATE INDEX idx_srer_fixture ON sky_rating_eval_result(fixture_name);
CREATE INDEX idx_srun_timestamp ON sky_rating_eval_run(run_timestamp);
